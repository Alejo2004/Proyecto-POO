    public void actualizarTabla(DefaultTableModel datos) {
        // Eliminar las filas existentes
        while (modelo.getRowCount() > 0) {
        modelo.removeRow(0);
        }

        // Copiar los datos del modelo pasado al modelo de la tabla de clasificaci贸n
        for (int i = 0; i < datos.getRowCount(); i++) {
            String[] fila = new String[6];  // 6 columnas en la tabla de clasificaci贸n
            fila[0] = datos.getValueAt(i, 0).toString();  // Equipo
            fila[1] = "0";  // PJ
            fila[2] = "0";  // GF
            fila[3] = "0";  // GR
            fila[4] = "0";  // GD
            fila[5] = "0";  // PUNTOS
        
            modelo.addRow(fila);
        }
    }

